public class Triangle {
    double basis;
    double secondFLank;
    double thirdFLank;
    double height;

    Triangle(double basis, double secondFLank, double thirdFLank, double height) {
        this.basis = basis;
        this.secondFLank = secondFLank;
        this.thirdFLank = thirdFLank;
        this.height = height;
    }
}
